package pers.linhai.nature.esaccessor.exception;

/**
 * 配置文件解析异常
 * 
 * @author admin
 * 
 */
public class TypeAccessorException extends RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public TypeAccessorException()
	{
		super();
	}

	public TypeAccessorException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public TypeAccessorException(String message)
	{
		super(message);
	}

	public TypeAccessorException(Throwable cause)
	{
		super(cause);
	}

}
